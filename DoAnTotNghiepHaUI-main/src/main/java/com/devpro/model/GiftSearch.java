package com.devpro.model;

public class GiftSearch {
	private Integer id;
	private String title;
	private String giftSeo;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getGiftSeo() {
		return giftSeo;
	}
	public void setGiftSeo(String giftSeo) {
		this.giftSeo = giftSeo;
	}
	
	
}
